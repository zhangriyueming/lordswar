function url(){
    var $link = prompt("Gib die URL der Webseite ein.", "http://");
    var $report = prompt("Optional kannst du auch einen Namen fuer die Webseite eingeben.", "");
    if($report == ""){
     $report = $link;
    }
    if($link == "" || $link == "http://"){
         document.mailshow.message.value =  document.mailshow.message.value;  
    }else {
        var $urlAll = "[url=" + $link + "]" + $report + "[/url]";
        document.mailshow.message.value = document.mailshow.message.value + $urlAll;    
    }
    

}